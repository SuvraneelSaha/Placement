public class O11_PrettyPrinting {
    public static void main(String[] args) {
        float a = 123.21321f;
        System.out.printf("Formatted number is %.2f",a);
        // % means a place holder
        // .2f means upto 2 decimal numbers
        // printf -- formatted string

        // example the value of pi upto 3 places
        System.out.println();

        System.out.printf("value of pie : %.3f",Math.PI);

        System.out.println();

        System.out.printf("Hello my name is %s and my hobby is %s","Nigga","Gaming");

        // the order in which we have placed the placeholders ie %
        // in that order only we have place the

    }
}
